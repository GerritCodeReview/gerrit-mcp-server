from typing import Any, Dict, List


def sort_changes_by_date(changes: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """Sorts a list of changes by date."""
    return sorted(changes, key=lambda k: k["updated"], reverse=True)